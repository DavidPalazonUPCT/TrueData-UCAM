def reconstruct_label(timestamp, label):
    """
        Reconstructs a label array based on the given timestamps.

        This function takes a list of timestamps and corresponding labels, sorts the timestamps,
        and reconstructs the label array to match the intervals of the sorted timestamps.
        The reconstruction involves creating a new label array where each unique timestamp interval
        corresponds to the provided labels.

        Parameters:
        -----------
        timestamp : array-like
            The original timestamps associated with each label, which can be of any numerical type.
        label : array-like
            The labels corresponding to the original timestamps, indicating the presence (1) or absence (0)
            of a particular event.

        Returns:
        --------
        numpy.ndarray
            A reconstructed label array where the labels are aligned to the intervals of the sorted timestamps.
            The output has a length equal to the number of unique intervals between the timestamps.
    """
    timestamp = np.asarray(timestamp, np.int64)
    index = np.argsort(timestamp)

    timestamp_sorted = np.asarray(timestamp[index])
    interval = np.min(np.diff(timestamp_sorted))

    label = np.asarray(label, np.int64)
    label = np.asarray(label[index])

    idx = (timestamp_sorted - timestamp_sorted[0]) // interval

    new_label = np.zeros(shape=((timestamp_sorted[-1] - timestamp_sorted[0]) // interval + 1,), dtype=int)
    new_label[idx] = label

    return new_label


def label_evaluation(truth_list, result_list, delay=7):
    """
        Evaluates the performance of predicted labels against ground truth labels.

        This function reconstructs the labels from the given truth and prediction lists,
        applies a delay to the predicted labels to account for early detection evaluation,
        and computes the F1 score based on the reconstructed labels.

        Parameters:
        -----------
        truth_list : array-like
            A list of ground truth labels (0 or 1) indicating the presence or absence of an event.
        result_list : array-like
            A list of predicted labels (0 or 1) generated by the model for comparison against the ground truth.
        delay : int, optional
            The number of time steps to consider for early detection evaluation. Default is 7.

        Returns:
        --------
        float
            The F1 score computed from the ground truth and predicted labels, reflecting the accuracy of the predictions.
    """
    truth_df = {'timestamp': range(len(truth_list)), 'label': truth_list}
    result_df = {'timestamp': range(len(result_list)), 'predict': result_list}

    y_true_list = []
    y_pred_list = []
    # Adapted from: https://arxiv.org/pdf/1906.03821.pdf
    truth = truth_df
    y_true = reconstruct_label(truth["timestamp"], truth["label"])
    result = result_df
    y_pred = reconstruct_label(result["timestamp"], result["predict"])
    y_pred = get_range_proba(y_pred, y_true, delay)
    y_true_list.append(y_true)
    y_pred_list.append(y_pred)

    # run f1score
    fscore = f1_score(np.concatenate(y_true_list), np.concatenate(y_pred_list))
    return fscore

